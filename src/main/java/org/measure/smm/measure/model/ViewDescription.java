package org.measure.smm.measure.model;

public class ViewDescription {
	
	
	private String text;
	private String html;
	
	public String getText() {
		return text;
	}
	
	public void setText(String text) {
		this.text = text;
	}
	
	public String getHtml() {
		return html;
	}
	
	public void setHtml(String html) {
		this.html = html;
	}
	
	

}
